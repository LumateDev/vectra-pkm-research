def make_note(note_id: int, topic: str, index: int, random: Random) -> dict[str, str | int]:
    config = TOPICS[topic]
    title = f"{random.choice(config['titles'])} #{index + 1}"
    terms = random.sample(config["terms"], 2)
    template = random.choice(config["templates"])
    content = template.format(a=terms[0], b=terms[1])
    content += " " + random.choice(
        [
            "Запись выглядит как рабочий фрагмент личной базы знаний, а не как готовая статья.",
            "Эту заметку можно использовать в эксперименте по качеству поиска.",
            "Формат специально короткий, похожий на реальную заметку в PKM-системе.",
            "В тексте достаточно контекста, чтобы semantic search мог найти смысловую связь.",
        ]
    )
    return {
        "id": note_id,
        "title": title,
        "content": content,
        "topic": topic,
        "source": "synthetic_pkm_ru",
    }


def main() -> None:
    random = Random(43)
    rows: list[dict[str, str | int]] = []
    note_id = 1

    for topic in TOPICS:
        for index in range(30):
            rows.append(make_note(note_id, topic, index, random))
            note_id += 1

    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT_PATH.open("w", encoding="utf-8", newline="") as file:
        writer = csv.DictWriter(file, fieldnames=["id", "title", "content", "topic", "source"])
        writer.writeheader()
        writer.writerows(rows)

    print(f"Generated {len(rows)} Russian notes at {OUTPUT_PATH}")


if __name__ == "__main__":
    main()

