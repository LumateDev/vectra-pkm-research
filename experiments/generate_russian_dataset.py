NOTE_FORMS = ["short", "long", "idea", "technical", "todo", "lecture", "research", "reflection"]


def sample_pair(values: list[str], random: Random) -> tuple[str, str]:
    first, second = random.sample(values, 2)
    return first, second


def make_title(config: dict[str, list[str]], index: int, form: str, random: Random) -> str:
    prefixes = {
        "short": "Короткая заметка",
        "long": "Развёрнутая запись",
        "idea": "Рабочая идея",
        "technical": "Техническая заметка",
        "todo": "TODO",
        "lecture": "Конспект",
        "research": "Исследовательская запись",
        "reflection": "Размышление",
    }
    base = random.choice(config["titles"])
    return f"{prefixes[form]}: {base} #{index + 1}"


def make_content(config: dict[str, list[str]], form: str, random: Random) -> str:
    a, b = sample_pair(config["terms"], random)
    question = random.choice(config["questions"])
    action = random.choice(config["actions"])

    if form == "short":
        return f"{question} Важно проверить связь: {a} и {b}."

    if form == "todo":
        return "\n".join(
            [
                f"TODO по теме: {a}.",
                f"- {action};",
                f"- найти пример, где встречается {b};",
                "- после проверки перенести выводы в раздел эксперимента.",
            ]
        )

    if form == "technical":
        return "\n".join(
            [
                f"Техническая фиксация: для этой части прототипа важны {a} и {b}.",
                "Входные данные должны оставаться простыми: заголовок, текст, тема и источник.",
                f"Проверка: {action}.",
                "Если результат воспроизводится на CSV-наборе, его можно использовать в отчёте.",
            ]
        )

    if form == "lecture":
        return "\n".join(
            [
                f"Конспект: {a}.",
                f"1. Основная идея связана с тем, что {b} влияет на качество работы с заметками.",
                f"2. Вопрос для обсуждения: {question}",
                f"3. Практический вывод: {action}.",
            ]
        )

    if form == "research":
        return (
            f"Наблюдение для исследования: {question} "
            f"В эксперименте нужно отдельно смотреть на {a}, потому что этот признак может менять выдачу. "
            f"Связанный фактор — {b}. Рабочее действие: {action}. "
            "Вывод пока предварительный, его нельзя переносить на реальные базы знаний без дополнительной проверки."
        )

    if form == "reflection":
        return (
            f"Поймал мысль во время работы с заметками: иногда важнее быстро восстановить контекст, чем идеально всё разложить. "
            f"Здесь всплывают {a} и {b}. {question} "
            "Возможно, ценность PKM-прототипа именно в том, что он помогает вернуться к забытой связи между идеями."
        )

    if form == "idea":
        return (
            f"Идея: использовать {a} как опорный сигнал, а {b} оставить как дополнительный признак. "
            f"Пользователь не должен вручную поддерживать сложную структуру. "
            f"Минимальный следующий шаг: {action}. "
            "Если решение остаётся понятным без отдельного backend и базы данных, оно подходит для исследовательского прототипа."
        )

    paragraphs = [
        f"Эта запись собрана как более длинный фрагмент личной базы знаний. Центральный вопрос: {question}",
        (
            f"В текущем прототипе важно не усложнять реализацию, но сохранить возможность проверить {a}. "
            f"Отдельно стоит учитывать {b}, потому что пользовательские запросы часто формулируются неточно."
        ),
        (
            f"Практический шаг: {action}. После этого можно посмотреть, изменится ли качество поиска "
            "и насколько удобно читать результаты в интерфейсе."
        ),
    ]
    return "\n\n".join(paragraphs)


def make_note(note_id: int, topic: str, index: int, random: Random) -> dict[str, str | int]:
    config = TOPICS[topic]
    form = NOTE_FORMS[index % len(NOTE_FORMS)]
    if index % 11 == 0:
        form = random.choice(NOTE_FORMS)

    return {
        "id": note_id,
        "title": make_title(config, index, form, random),
        "content": make_content(config, form, random),
        "topic": topic,
        "source": "synthetic_pkm_ru_diverse",
    }


def main() -> None:
    random = Random(43)
    rows: list[dict[str, str | int]] = []
    note_id = 1

    for topic in TOPICS:
        for index in range(NOTES_PER_TOPIC):
            rows.append(make_note(note_id, topic, index, random))
            note_id += 1

    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT_PATH.open("w", encoding="utf-8", newline="") as file:
        writer = csv.DictWriter(file, fieldnames=["id", "title", "content", "topic", "source"])
        writer.writeheader()
        writer.writerows(rows)

    print(f"Generated {len(rows)} Russian notes at {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
